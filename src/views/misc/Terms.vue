    <h2>2. MorphoBank Services</h2>
    <p>
      MorphoBank is a web application and database that provides scientists
      conducting comparative biological or paleontological research worldwide a
      private, online workspace into which they can load data (e.g., text;
      phylogenetic matrices, measurements, descriptions, and documents;
      metadata; and 2D and 3D media, sound and video) (collectively, “Data”),
      and then release such Data in conjunction with a peer-reviewed paper.
      Published MorphoBank data packages called “MorphoBank Projects” are then
      tagged with a digital object identifier (“DOI”). MorphoBank’s database
      includes fields for taxonomy that can be associated with different types
      of loaded Data, as well as a novel web-enabled phylogenetic matrix editor
      that allows teams to collaborate in real time and to load and archive Data
      in support of scientific observations. Data uploaded by a user is not
      visible to other MorphoBank users unless, and only if, the uploading user
      determines to publish such Data and clicks through the appropriate screens
      to “publish” the same. MorphoBank also has a novel web-based Media viewer
      that allows users to label anatomical features, and a graphical interface
      to record and display rules relating to phylogenetic characters. All of
      the foregoing functionality is referred to herein as the “Services”.
    </p>
